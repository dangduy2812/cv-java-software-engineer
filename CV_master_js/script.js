let currentLang = localStorage.getItem("cv-lang") || "en";
let currentTheme = localStorage.getItem("cv-theme") || "light";

/* ===== Init ===== */
document.addEventListener("DOMContentLoaded", () => {
  applyTheme(currentTheme);
  applyLanguage(currentLang);
  initThemeToggle();
  initLangToggle();
  initScrollReveal();
  initResumeToggles();
  initRatingAnimation();
});

/* ===== Theme ===== */
function applyTheme(theme) {
  currentTheme = theme;
  document.documentElement.setAttribute("data-theme", theme);
  localStorage.setItem("cv-theme", theme);
  const icon = document.getElementById("theme-icon");
  if (icon) {
    icon.className = theme === "dark" ? "icon-sun" : "icon-moon";
  }
}

function initThemeToggle() {
  const btn = document.getElementById("theme-toggle");
  if (!btn) return;
  btn.addEventListener("click", () => {
    applyTheme(currentTheme === "dark" ? "light" : "dark");
  });
}

/* ===== Language ===== */
function applyLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("cv-lang", lang);
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });

  document.querySelectorAll(".resume-toggle").forEach((btn) => {
    const card = btn.closest(".resume-card");
    if (card && card.classList.contains("expanded")) {
      btn.textContent = translations[lang].view_less;
    } else {
      btn.textContent = translations[lang].view_more;
    }
  });
}

function initLangToggle() {
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", () => applyLanguage(btn.dataset.lang));
  });
}

/* ===== Email gate ===== */
function handleSubmit() {
  const emailValue = document.getElementById("email").value.toLowerCase().trim();
  const errorEmail = document.getElementById("error-email");
  const regex =
    /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;

  const personalInfo = document.querySelector(".personal-info-hidden");
  const submitControl = document.querySelector(".email-gate");

  if (regex.test(emailValue)) {
    personalInfo.classList.add("visible");
    submitControl.style.display = "none";
    errorEmail.textContent = "";
  } else {
    errorEmail.textContent = translations[currentLang].email_error;
  }
}

/* ===== Resume card toggle ===== */
function initResumeToggles() {
  document.querySelectorAll(".resume-toggle").forEach((btn) => {
    btn.addEventListener("click", () => {
      const card = btn.closest(".resume-card");
      const isExpanded = card.classList.toggle("expanded");
      btn.textContent = isExpanded
        ? translations[currentLang].view_less
        : translations[currentLang].view_more;
    });
  });
}

/* ===== Scroll reveal ===== */
function initScrollReveal() {
  const prefersReduced = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  if (prefersReduced) {
    document.querySelectorAll(".reveal").forEach((el) => el.classList.add("visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("visible");
          const rating = entry.target.querySelector(".rating-bar");
          if (rating) rating.classList.add("animate");
        }
      });
    },
    { threshold: 0.15, rootMargin: "0px 0px -40px 0px" }
  );

  document.querySelectorAll(".reveal").forEach((el) => observer.observe(el));
}

/* ===== Rating bar animation on skills card expand ===== */
function initRatingAnimation() {
  document.querySelectorAll(".resume-card").forEach((card) => {
    const rating = card.querySelector(".rating-bar");
    if (!rating) return;
    const btn = card.querySelector(".resume-toggle");
    if (!btn) return;
    btn.addEventListener("click", () => {
      if (card.classList.contains("expanded")) {
        setTimeout(() => rating.classList.add("animate"), 100);
      }
    });
  });
}

/* ===== Demo pages shared init ===== */
function initDemoPage() {
  applyTheme(localStorage.getItem("cv-theme") || "light");
  applyLanguage(localStorage.getItem("cv-lang") || "en");

  const themeBtn = document.getElementById("theme-toggle");
  if (themeBtn) {
    themeBtn.addEventListener("click", () => {
      const t = document.documentElement.getAttribute("data-theme");
      applyTheme(t === "dark" ? "light" : "dark");
    });
  }

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", () => applyLanguage(btn.dataset.lang));
  });
}
